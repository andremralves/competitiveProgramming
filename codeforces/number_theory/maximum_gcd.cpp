using ll = int64_t;

ll ceil(ll a, ll b) {return a % b == 0 ? a / b : a / b + 1;}

void solve() {
    int n;
    cin>>n;
    if(n % 2) n -= 1;
    cout<<__gcd(n, n/2)<<"\n";
}

