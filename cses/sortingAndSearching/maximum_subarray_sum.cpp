void solve() {
    int n; cin>>n;
    ll sum = 0, ans = INT_MIN;
    for(int i=0; i<n; i++) {
        if(sum < 0) sum = 0;
        int x; cin>>x;
        sum += x;
        ans = max(ans, sum);
    }
    cout<<ans<<"\n";
}

int main ()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t = 1;
    while (t--) solve();
    return 0;
}
