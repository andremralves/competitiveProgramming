void solve() {
    int n;
    cin>>n;
    vector<int> a(n);
    for(int i=0; i<n; i++) {
        cin>>a[i];
    }

    int mi = *min_element(all(a));
    int ma = *max_element(all(a));

    int ans = 1e6;
    for(int j=mi; j<=ma; j++) {
        int cnt = 0;
        for(int i=0; i<n; i++) {
            int x = (a[i]-j);
            cnt += x*x;
        }
        ans = min(ans, cnt);
    }
    cout<<ans<<"\n";
}

int main ()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t = 1;
    while (t--) solve();
    return 0;
}
