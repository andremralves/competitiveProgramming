ll calcSum(ll val) {
    return (val-1)*val/2;
}

void solve() {
    ll n;
    cin>>n;

    ll lo = 0, hi = 2e9+1;
    while(hi - lo > 1) {
        ll mid = lo+(hi-lo)/2;
        ll sum = calcSum(mid);

        if(sum < n) {
            lo = mid;
        } else {
            hi = mid;
        }
    }
    if(calcSum(hi) == n)
        cout<<hi<<"\n";
    else
        cout<<lo+max((ll)0, n-calcSum(lo))<<"\n";
}
