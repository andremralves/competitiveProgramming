void solve() {
    int n;
    cin>>n;
    vector<int> a(n);
    for(int i=0;i<n;i++) {
        cin>>a[i];
    }

    if(a[0] > 2e5) {
        cout<<"NO\n";
        return;
    }

    vector<int> b;
    int cnt = 0;
    for(int i=n-1; i>=0; i--) {
        for(int j=0; j<a[i]-cnt; j++) {
            b.push_back(i+1);
        }
        if(a[i] - cnt <= 0)
            continue;
        cnt+=(a[i]-cnt);
    }
    dbg(a);
    dbg(b);
    if(a == b) {
        cout<<"YES\n";
    } else {
        cout<<"NO\n";
    }
}

